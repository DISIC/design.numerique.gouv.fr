        <h3>Le RGAA</h3>

          <p>
            Le RGAA harmonise les normes internationales connues sous l’appellation de WCAG. Cette 4e version du RGAA ne reprend que la partie obligatoire des normes harmonisées correspondant au niveau A et AA de WCAG. Les recommandations relevant du niveau AAA de WCAG seront reprises dans un document à caractère non réglementaire (publication courant 2020). Le RGAA 4 fait disparaître aussi la distinction entre niveau A et AA qui n’est plus justifiée puisque toutes ces règles sont obligatoires. Il y ajoute des critères de test permettant de vérifier leur bonne application.
          </p>

          <p>
            La méthode technique du RGAA permet de vérifier qu’une page web – c’est-à-dire tout contenu HTML (HTML4, XHTML1 et HTML5) - est conforme aux 50 critères de succès des niveaux A et AA de la norme internationale WCAG 2.1 qui ont été retenus dans la norme européenne de référence pour établir le niveau d’exigence légale en matière d’accessibilité numérique. La méthode technique du RGAA propose un cadre opérationnel de vérification de la conformité aux exigences d'accessibilité. Elle comporte 106 critères de contrôle RGAA incluant une moyenne de 2,5 tests par critères. Certains tests font référence à des techniques d'implémentation (HTML, CSS, JavaScript...) pour vérifier que le critère est respecté afin de réduire la marge d’interprétation quant au respect des normes d’accessibilité.
          </p>

          <p>
            En cas d'absence de mise à jour du référentiel sous 3 ans pour prendre en compte de nouveaux standards ou spécifications techniques, il est possible de créer ses propres tests en complément de ceux existant. Certains tests, concernant notamment les composants d’interface développés en JavaScript, requièrent de vérifier la restitution des contenus avec des technologies d’assistance associées à des navigateurs et des systèmes d’exploitation.
          </p>

          <p>
            À noter que la méthode technique du RGAA 4.0 ne couvre pas les applications mobiles natives, les progiciels et le mobilier urbain numérique pour lesquels il sera nécessaire de vérifier directement la mise en œuvre de la norme de référence EN 301-549 V2.1.2.
          </p>

      </section>

